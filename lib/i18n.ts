export function getTranslation(lang: Language, key: string): string {
  const keys = key.split(".");
  let value: any = translations[lang];

  for (const k of keys) {
    value = value?.[k];
  }

  return value || key;
}

export function useTranslation(lang: Language) {
  return (key: string) => getTranslation(lang, key);
}
